#[cfg(test)]
mod tests {
    use super::*;
    use crate::parse::EventDecl;
    use syn::parse_str;

    #[test]
    fn canonical_sig_transfer() {
        let decl: EventDecl = parse_str(
            "event Transfer(address indexed from, address indexed to, uint256 value);",
        )
        .unwrap();
        assert_eq!(canonical_signature(&decl), "Transfer(address,address,uint256)");
    }

    #[test]
    fn canonical_sig_univ2_swap() {
        let decl: EventDecl = parse_str(
            "event Swap(address indexed sender, uint256 amount0In, uint256 amount1In, \
             uint256 amount0Out, uint256 amount1Out, address indexed to);",
        )
        .unwrap();
        assert_eq!(
            canonical_signature(&decl),
            "Swap(address,uint256,uint256,uint256,uint256,address)"
        );
    }

    #[test]
    fn keccak_transfer_at_expansion_time() {
        let hash = keccak256_bytes(b"Transfer(address,address,uint256)");
        assert_eq!(
            hex::encode(hash),
            "ddf252ad1be2c89b69c2b068fc378daa952ba7f163c4a11628f55a4df523b3ef"
        );
    }

    #[test]
    fn keccak_univ2_swap_at_expansion_time() {
        let hash = keccak256_bytes(b"Swap(address,uint256,uint256,uint256,uint256,address)");
        assert_eq!(
            hex::encode(hash),
            "d78ad95fa46c994b6551d0da85fc275fe613ce37657fb8d5e3d130840159d822"
        );
    }
}
